#include <stdio.h>

int main() {
    int num1, num2, sum;

    // Input
    printf("Enter the first integer: ");
    scanf("%d", &num1);

    printf("Enter the second integer: ");
    scanf("%d", &num2);

    // Addition
    sum = num1 + num2;

    // Output
    printf("Sum: %d\n", sum);

    return 0;
}
